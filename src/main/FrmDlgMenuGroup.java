    private void simpan() throws SQLException{
        if(!cekSebelumSimpan())
            return;
        if(oldId.length() > 0){ //update
            String sUpd="UPDATE m_menu_group\n" +
                    "   SET group_id=?, keterangan=?, module_name=?\n" +
                    " WHERE group_id=?;";
            PreparedStatement ps=conn.prepareStatement(sUpd);
            ps.setString(1, txtID.getText());
            ps.setString(2, txtKeterangan.getText());
            ps.setString(3, "APOTEK");
            ps.setString(3, oldId);
            ps.execute();
        }
        else{ //update
            String sUpd="INSERT INTO m_menu_group(\n" +
            "            group_id, keterangan, module_name)\n" +
            "    VALUES (?, ?, ?);";
            PreparedStatement ps=conn.prepareStatement(sUpd);
            ps.setString(1, txtID.getText());
            ps.setString(2, txtKeterangan.getText());
            ps.setString(3, "APOTEK");
            
            ps.execute();
        }
    }

