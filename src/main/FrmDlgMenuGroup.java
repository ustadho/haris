    private boolean cekSebelumSimpan() {
        btnSave.requestFocusInWindow();
        if(txtID.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Masukkan ID terlebih dulu!");
            txtID.requestFocus();
            return false;
        }
        if(txtKeterangan.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Masukkan Keterangan terlebih dulu!");
            txtKeterangan.requestFocus();
            return false;
        }
        
        return true;
    }
