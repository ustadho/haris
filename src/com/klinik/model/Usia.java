public class Usia {
    private double tahun;
    private double bulan;
    private double hari;

    public double getTahun() {
        return tahun;
    }

    public void setTahun(double tahun) {
        this.tahun = tahun;
    }

    public double getBulan() {
        return bulan;
    }

    public void setBulan(double bulan) {
        this.bulan = bulan;
    }

    public double getHari() {
        return hari;
    }

    public void setHari(double hari) {
        this.hari = hari;
    }
    
}
